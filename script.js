function renderArticle(articleData) {
  var container = document.getElementById("modal-article");
  container.innerHTML = "";

  articleData.forEach(function (block) {
    var el;
    if (block.t === "h") {
      el = document.createElement("h3");
      el.textContent = block.v;
      el.className = "font-orbitron text-neon-light text-[10px] tracking-[.25em] uppercase mt-6 mb-2 pb-2 border-b border-rim";
    } else {
      el = document.createElement("p");
      el.textContent = block.v;
      el.className = "text-slate-400 text-sm leading-relaxed mb-0";
    }
    container.appendChild(el);
  });
}

function renderCards(data) {
  var grid = document.getElementById("ost-grid");
  var empty = document.getElementById("empty-state");

  grid.innerHTML = "";

  if (data.length === 0) {
    empty.style.display = "block";
    return;
  }

  empty.style.display = "none";

  data.forEach(function (ost, index) {
    grid.appendChild(createCard(ost, index));
  });
}

function openModal(ost) {
  var cfg = getConfig(ost.categorie);
  var modal = document.getElementById("modal");
  var inner = document.getElementById("modal-inner");

  document.getElementById("modal-titre").textContent = ost.titre;
  document.getElementById("modal-jeu").textContent = ost.jeu;
  document.getElementById("modal-compositeur").textContent = ost.compositeur;
  document.getElementById("modal-description").textContent = ost.description;

  var badge = document.getElementById("modal-badge");
  badge.textContent = ost.categorie;
  badge.style.backgroundColor = cfg.badgeBg;
  badge.style.color = cfg.badgeColor;

  document.getElementById("yt-frame").src = "https://www.youtube.com/embed/" + ost.youtubeId + "?autoplay=1";

  renderArticle(ost.article);

  var panel = document.getElementById("modal-article-panel");
  if (panel) { panel.scrollTop = 0; }

  modal.style.display = "block";
  document.body.style.overflow = "hidden";
  modal.scrollTop = 0;

  inner.style.animation = "none";
  void inner.offsetHeight;
  inner.style.animation = "";
}

function closeModal() {
  document.getElementById("modal").style.display = "none";
  document.getElementById("yt-frame").src = "";
  document.body.style.overflow = "";
}

function filterAndRender() {
  var filtered = ostData.filter(function (ost) {
    var matchCategorie = currentFilter === "Tous" || ost.categorie === currentFilter;
    var matchSearch = ost.titre.toLowerCase().indexOf(currentSearch) !== -1 || ost.jeu.toLowerCase().indexOf(currentSearch) !== -1;
    return matchCategorie && matchSearch;
  });
  renderCards(filtered);
}

document.addEventListener("DOMContentLoaded", function () {
  renderCards(ostData);

  document.getElementById("search-input").addEventListener("input", function (e) {
    currentSearch = e.target.value.trim().toLowerCase();
    filterAndRender();
  });

  document.getElementById("btn-explore").addEventListener("click", function () {
    document.getElementById("collection").scrollIntoView({ behavior: "smooth" });
  });

  document.getElementById("scroll-hint").addEventListener("click", function () {
    document.getElementById("collection").scrollIntoView({ behavior: "smooth" });
  });

  document.getElementById("filters").addEventListener("click", function (e) {
    var btn = e.target.closest(".filter-btn");
    if (!btn) return;

    document.querySelectorAll(".filter-btn").forEach(function (b) {
      b.classList.remove("filter-active");
    });

    btn.classList.add("filter-active");
    currentFilter = btn.dataset.filter;
    filterAndRender();
  });

  document.getElementById("btn-top").addEventListener("click", function () {
    window.scrollTo({ top: 0, behavior: "smooth" });
  });

  document.getElementById("btn-close").addEventListener("click", closeModal);
  document.getElementById("modal-overlay").addEventListener("click", closeModal);

  document.addEventListener("keydown", function (e) {
    if (e.key === "Escape") { closeModal(); }
  });
});